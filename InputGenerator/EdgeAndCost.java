
public class EdgeAndCost {

	int edge;
	int cost;
	
	
	public EdgeAndCost(int edge, int cost) {
		
		this.edge = edge;
		this.cost = cost;
	}
	
	public int getEdge() {
		return edge;
	}
	
	public void setEdge(int edge) {
		this.edge = edge;
	}
	
	public int getCost() {
		return cost;
	}
	
	public void setCost(int cost) {
		this.cost = cost;
	}
	
}
